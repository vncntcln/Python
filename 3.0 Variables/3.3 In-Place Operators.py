# x = x + 3 written as x += 3
x = 2
print(x)

x += 3
print(x)

x = 8

# Minus In-Place Operators
x -= 2
print(x)

# Divide In-Place Operators
x /= 3
print(x)

# Exponential In-Place Operators
x **= 5
print(x)

# Concatenation In-Place Operators
x = "Spam"
print(x)

x += "eggs"
print(x)