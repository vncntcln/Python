# Operation within the {}
a = 5
b = 10
print(f'Test if i can add {a} and {b} and have the result {a + b}.')

# Add comma as separators
number = 1234567890
print(f'{number}')
print(f'{number,}')
print(f'{number:,}')

# IF-Else Clause
x = 1
print(f"Lets test the value: {True if x == 1 else False}")
