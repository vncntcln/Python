# Backslash '\'
print("Nany\'s House")
print("\"Cool\"")

# Newlines '\n'
print("One \nTwo \nThree")

# Tab '\t'
print("\tThis is tab")

# Carriage Return '\r'
print("Hello \rWorld")

# Escaping special character '\n'
print("This is newlines \\n")

# Multilines
print("""This 
is 
Multilines""")
